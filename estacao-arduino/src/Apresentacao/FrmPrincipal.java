    private void btnLigarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLigarActionPerformed
        if (Estaticos.coletaSerial){
            Estaticos.coletaSerial = false;
            this.btnLigar.setText("Ligar");
        } else {
            Estaticos.coletaSerial = true;
            Serial serial = new Serial();
            Thread t = new Thread(serial);
            t.start();
            this.btnLigar.setText("Desligar");
        }
    }//GEN-LAST:event_btnLigarActionPerformed

